  # 调用函数
# 传入参数是，在计算时，接受外部调用提供的数据。

def add(x,y):  
    #  x and y 是形式参数，表示函数将要使用2个参数。
    
    result = x + y
    print(f"{x} + {y} 的结果是：{result}")
add(78,89)
