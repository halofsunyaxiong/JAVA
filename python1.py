# 参数传入的案例
def check(z):  
    if z <= 37.5:
        print(f"欢迎来到黑马程序员!请你出示健康码和72小时的核酸证明,并配合测量体温。\n测量体温中,你的体温是:{z}度,体温正常，请进入。")
    else:
         print(f"欢迎来到黑马程序员!请你出示健康码和72小时的核酸证明,并配合测量体温。\n测量体温中,你的体温是:{z}度,需要隔离。")    
check(39.0)


